package Lec01;

public class Variables {

    public static void main(String[] args) {
        int x_2 = 2;
        System.out.println(x_2);
        int a = 9;
        int b = 7;
        int c = a + b;
        System.out.println(c);
        c = a - b;
        System.out.println(c);
        ++a;
        System.out.println(a);
        --a;
        System.out.println(a);
    }
}
